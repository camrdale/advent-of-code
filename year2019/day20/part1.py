from aoc.input import InputParser
from aoc import log
from aoc.runner import Part

from year2019.day20.shared import PlutoMap


class Part1(Part):
    def run(self, parser: InputParser) -> int:
        input = parser.get_input()

        map = PlutoMap(input)
        log.log(log.INFO, map.print_map())

        _, shortest_path = map.shortest_paths(map.portals['A'][0], map.portals['Z'][0], '# ')
        if shortest_path is None:
            raise ValueError(f'Failed to find the shortest path')

        log.log(log.INFO, map.print_map({'@': shortest_path.previous}, additional_feature_priority=False))

        log.log(log.RESULT, f'Shortest path from A to Z: {shortest_path.length}')
        return shortest_path.length


