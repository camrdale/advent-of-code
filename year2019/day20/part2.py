from aoc.input import InputParser
from aoc import log
from aoc.runner import Part

from year2019.day20.shared import PlutoMap, LevelLocation


class Part2(Part):
    def run(self, parser: InputParser) -> int:
        input = parser.get_input()

        map = PlutoMap(input)
        log.log(log.INFO, map.print_map())

        shortest_path = map.shortest_level_path(
            LevelLocation(map.portals['A'][0], 0),
            LevelLocation(map.portals['Z'][0], 0))
        if shortest_path is None:
            raise ValueError(f'Failed to find the shortest path')

        log.log(log.RESULT, f'Shortest path from the outermost A to the outermost Z: {shortest_path.length}')
        return shortest_path.length


