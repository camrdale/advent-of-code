from aoc.input import InputParser
from aoc import log
from aoc.runner import Part

from year2020.day20.shared import Tile


class Part1(Part):
    def run(self, parser: InputParser) -> int:
        input = parser.get_multipart_input()

        arranged, _, _ = Tile.arrange(input)

        log.log(log.INFO, arranged)

        corner_product = int(arranged[0,0]) * int(arranged[0,-1]) * int(arranged[-1,-1]) * int(arranged[-1,0])

        log.log(log.RESULT, f'The product of the IDs of the corner tiles: {corner_product}')
        return corner_product


